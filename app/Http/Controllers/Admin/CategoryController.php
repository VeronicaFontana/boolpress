    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        // Prima di creare una nuova categoria, verifichiamo se esiste già
        // Se esiste non ne creo una nuova, ma ritorno alla index con un messaggio di errore
        $exist = Category::where("name", $request->name)->first();

        if($exist){
            return redirect()->route("admin.categories.index")->with("error", "Categoria già presente");
        }else{
            $new_category = new Category();
            $new_category->name = $request->name;
            $new_category->slug = Str::slug($request->name, "-");
            $new_category->save();

            return redirect()->route("admin.categories.index")->with("success", "Categoria creata correttamente");
        }
    }

